#pragma once

#include "stack.hpp"  
#include <string>     
#include <sstream>    
#include <stdexcept>  
#include <cctype>     

// Класс для вычисления выражений в обратной польской нотации (RPN)
// Постфиксная нотация: операнды идут перед оператором, например: "2 3 +" вместо "2+3"
class PostfixCalculator {
public:
    // Основной метод: принимает строку с выражением в RPN, возвращает результат
    // Токены (числа и операторы) должны разделяться пробелами
    double evaluate(const std::string& expr) {
        // Стек для хранения операндов (промежуточных результатов)
        // Используем наш шаблонный Stack с типом double
        Stack<double> stack;
        
        // Создаём поток из строки для пошагового чтения токенов
        // Пробелы будут автоматически разделять токены
        std::istringstream iss(expr);
        std::string token;  // Буфер для хранения текущего токена

        // Читаем токены по одному, пока не закончится строка
        // iss >> token автоматически пропускает пробелы и читает следующий токен
        while (iss >> token) {
            // Проверяем, является ли токен оператором (+, -, *, /)
            if (isOperator(token)) {
                // Для бинарной операции нужно минимум 2 операнда на стеке
                if (stack.size() < 2)
                    throw std::invalid_argument(
                        "PostfixCalculator: недостаточно операндов для операции '" + token + "'");

                // Извлекаем правый операнд первым (вершина стека)
                double b = stack.pop();   // правый операнд (тот, что был добавлен позже)
                // Затем извлекаем левый операнд (был добавлен раньше)
                double a = stack.pop();   // левый операнд
                
                // Применяем операцию к операндам и кладём результат на стек
                // token[0] - берём первый символ строки (оператор)
                stack.push(applyOp(token[0], a, b));
            } else {
                // Токен не оператор -> пытаемся интерпретировать его как число
                
                // std::stod (string to double) пытается преобразовать строку в число
                // pos будет содержать количество обработанных символов
                std::size_t pos = 0;
                double num = std::stod(token, &pos);
                
                // Если не вся строка была преобразована, значит там есть лишние символы
                // Например: "123abc" → num=123, pos=3, но token.size()=6 → ошибка
                if (pos != token.size())
                    throw std::invalid_argument(
                        "PostfixCalculator: неизвестный токен '" + token + "'");
                
                // Число успешно распарсено - кладём его на стек
                stack.push(num);
            }
        }

        // После обработки всех токенов в стеке должен остаться ровно один элемент
        // Это и есть результат вычисления всего выражения
        if (stack.size() != 1)
            throw std::invalid_argument(
                "PostfixCalculator: некорректное выражение (остались лишние операнды в стеке)");

        // Извлекаем и возвращаем результат
        // После этого стек становится пустым (деструктор очистит память автоматически)
        return stack.pop();
    }

private:
    // Проверяет, является ли токен допустимым бинарным оператором
    // Токен считается оператором, если:
    // 1. Его длина равна 1 (все операторы у нас односимвольные)
    // 2. Этот символ присутствует в строке "+-*/"
    static bool isOperator(const std::string& t) {
        return t.size() == 1 && std::string("+-*/").find(t[0]) != std::string::npos;
        // std::string::npos - специальное значение, означающее "не найдено"
        // Если find вернул npos, значит символа нет в строке операторов
    }

    // Применяет арифметическую операцию к двум операндам
    // op - оператор (+, -, *, /)
    // a - левый операнд (был добавлен на стек раньше)
    // b - правый операнд (был добавлен на стек позже)
    // Возвращает результат операции
    static double applyOp(char op, double a, double b) {
        switch (op) {
            case '+':
                return a + b;   // сложение
                
            case '-':
                return a - b;   // вычитание
                
            case '*':
                return a * b;   // умножение
                
            case '/':
                // Проверка деления на ноль - математическая ошибка
                if (b == 0.0)
                    throw std::domain_error("PostfixCalculator: деление на ноль");
                return a / b;   // деление 
                
            default:
                // Если дошли сюда - значит isOperator пропустил недопустимый оператор
                // Такое возможно только при ошибке в логике isOperator
                throw std::invalid_argument(
                    std::string("PostfixCalculator: неподдерживаемая операция '") + op + "'");
        }
    }
};

/* 
 * ПОЧЕМУ RPN УДОБЕН ДЛЯ КОМПЬЮТЕРА:
 * 
 * - Не нужны скобки для указания приоритета операций
 * - Не нужен анализ приоритета операторов (+, * имеют разный приоритет)
 * - Простой линейный проход по токенам
 * - Используется только одна структура данных - стек
 * - Легко реализуется и отлаживается
 * 
 * Именно так работают компиляторы при генерации байт-кода
 */
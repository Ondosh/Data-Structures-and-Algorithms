    #include <iostream>
    #include <random>
    #include "templates.hpp"

    /**
     * @brief Генерирует динамический массив целых чисел с монотонно возрастающими значениями.
     * 
     * Функция создаёт массив, где первый элемент — случайное число из диапазона 
     * [least; 0], а каждый следующий увеличивается на случайную 
     * величину из [0; max_step_width]. Это гарантирует, что итоговый массив будет отсортирован.
     * 
     * @param n Размер генерируемого массива.
     * @return int* Указатель на новый массив в динамической памяти.
     * 
     * @warning Память выделяется через new[]. Освобождение памяти — обязанность вызывающей стороны.
     * @note При n == 0 возвращается указатель на пустой массив (размер 0).
     * 
     * @example
     * int* data = gen_random_array(100);
     * // ... использование ...
     * delete[] data;  // Не забыть освободить память!
     */
    int* gen_random_sorted_array(std::size_t n, int least, int max_step_width) {
        // Источник энтропии для инициализации генератора
        std::random_device rd;

        // Выделение памяти под массив
        int* arr = new int[n];

        // Генератор случайных чисел Mersenne Twister с seed от random_device
        std::mt19937 gen(rd());

        // Распределение для первого элемента: большой отрицательный диапазон
        std::uniform_int_distribution<> distr(least, 0);
        arr[0] = distr(gen);

        // Распределение для шагов приращения: [1; 1'000'000]
        std::uniform_int_distribution<> distr_cycle(1, max_step_width);
        
        // Формируем монотонную последовательность
        for (std::size_t i = 1; i < n; ++i) {
            arr[i] = arr[i - 1] + distr_cycle(gen);
        }
        
        return arr;
    }
    /**
   * @brief Генерирует динамический массив случайных целых чисел в заданном диапазоне.
   *
   * Функция создает массив указанного размера, заполняя его псевдослучайными числами,
   * равномерно распределенными в интервале [least, max]. Массив не гарантирует
   * какой-либо сортировки - порядок элементов полностью случайный.
   *
   * @param n Количество элементов в массиве. Должно быть > 0.
   * @param least Нижняя граница диапазона случайных чисел (включительно).
   * @param max Верхняя граница диапазона случайных чисел (включительно).
   *
   * @return int* Указатель на первый элемент динамически выделенного массива.
   *
   * @warning Выделенная память должна быть освобождена с помощью оператора `delete[]`.
   * @warning Функция не проверяет корректность параметра n (n > 0 - ответственность вызывающего).
 */
    int* gen_random_unsorted_array(std::size_t n, int least, int max) {
        // Источник энтропии для инициализации генератора
        std::random_device rd;

        // Выделение памяти под массив
        int* arr = new int[n];

        // Генератор случайных чисел Mersenne Twister с seed от random_device
        std::mt19937 gen(rd());

        // Распределение для всего диапазона [least; max]
        std::uniform_int_distribution<> distr(least, max);

        // Заполняем массив случайными числами
        for (std::size_t i = 0; i < n; ++i) {
            arr[i] = distr(gen);
        }

        return arr;
    }